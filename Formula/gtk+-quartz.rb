require 'formula'

class GtkxQuartz < Formula
  homepage 'http://gtk.org/'
  url 'http://ftp.gnome.org/pub/gnome/sources/gtk+/2.24/gtk+-2.24.17.tar.xz'
  sha256 'e947b2b460156d98e8e38930b82458e1d613a71eb26e927f966f7081a640f415'

  depends_on 'pkg-config' => :build
  depends_on 'xz' => :build
  depends_on 'glib'
  depends_on 'jpeg'
  depends_on 'libtiff'
  depends_on 'gdk-pixbuf'
  depends_on 'pango-quartz'
  depends_on 'jasper' => :optional
  depends_on 'atk'
  depends_on 'cairo-quartz'

  fails_with :llvm do
    build 2326
    cause "Undefined symbols when linking"
  end

  def install
    system "./configure", "--disable-dependency-tracking",
                          "--prefix=#{prefix}",
                          "--disable-glibtest",
                          "--disable-introspection",
                          "--disable-visibility",
                          "--with-gdktarget=quartz"

    system "make install"
  end

  def test
    system "#{bin}/gtk-demo"
  end
end
